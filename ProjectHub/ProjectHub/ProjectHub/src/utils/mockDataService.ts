  const projectIds = [...new Set(mockTasks.map(t => t.projectId))];
  
  projectIds.forEach(projectId => {
    const projectTasks = mockTasks.filter(t => t.projectId === projectId);
    
    // Separate root tasks (no parent) and child tasks
    const rootTasks = projectTasks.filter(t => !t.parentTaskId);
    const childTasks = projectTasks.filter(t => t.parentTaskId);
    
    // Create a dependency graph for root tasks
    const orderTasksByDependencies = (tasks: Task[]) => {
      const orderedTasks: Task[] = [];
      const visited = new Set<number>();
      const visiting = new Set<number>();
      
      const visit = (task: Task) => {
        if (visiting.has(task.id)) {
          // Circular dependency detected, just add the task
          return;
        }
        if (visited.has(task.id)) {
          return;
        }
        
        visiting.add(task.id);
        
        // Visit all prerequisites first
        if (task.predecessorIds) {
          task.predecessorIds.forEach(depId => {
            const depTask = tasks.find(t => t.id === depId);
            if (depTask && !visited.has(depId)) {
              visit(depTask);
            }
          });
        }
        
        visiting.delete(task.id);
        visited.add(task.id);
        orderedTasks.push(task);
      };
      
      tasks.forEach(task => {
        if (!visited.has(task.id)) {
          visit(task);
        }
      });
      
      return orderedTasks;
    };
    
    // Order root tasks by dependencies and start dates
    const orderedRootTasks = orderTasksByDependencies(rootTasks).sort((a, b) => {
      // Secondary sort by start date if no dependency relationship
      const aDate = new Date(a.startDate || '2024-01-01');
      const bDate = new Date(b.startDate || '2024-01-01');
      return aDate.getTime() - bDate.getTime();
    });
    
    // Assign task orders to root tasks
    orderedRootTasks.forEach((task, index) => {
      task.taskOrder = (index + 1) * 1000; // 1000, 2000, 3000, etc.
    });
    
    // Process child tasks for each parent
    orderedRootTasks.forEach(parentTask => {
      const children = childTasks.filter(t => t.parentTaskId === parentTask.id);
      if (children.length > 0) {
        // Order children by dependencies within the parent
        const orderedChildren = orderTasksByDependencies(children).sort((a, b) => {
          // Secondary sort by start date if no dependency relationship
          const aDate = new Date(a.startDate || '2024-01-01');
          const bDate = new Date(b.startDate || '2024-01-01');
          return aDate.getTime() - bDate.getTime();
        });
        
        // Assign task orders to children, starting from parent's order + small increments
        orderedChildren.forEach((child, index) => {
          child.taskOrder = (parentTask.taskOrder || 1000) + (index + 1) * 100; // e.g., 1100, 1200, 1300
        });
      }
    });
  });
  
