  // Task reordering methods
  static async reorderTask(request: TaskReorderRequest): Promise<Task> {
    await delay(300);
    
    const { taskId, newPosition, parentTaskId, projectId } = request;
    
    // Find the task to reorder
    const taskIndex = mockTasks.findIndex(t => t.id === taskId);
    if (taskIndex === -1) {
      throw new Error(`Task with ID ${taskId} not found`);
    }
    
    // Get siblings (tasks in same project and parent)
    const siblings = mockTasks.filter(t => 
      t.projectId === projectId && 
      t.parentTaskId === parentTaskId
    ).sort((a, b) => (a.taskOrder || 0) - (b.taskOrder || 0));
    
    // Remove the task being moved from siblings
    const movingTask = mockTasks[taskIndex];
    const siblingsWithoutMoving = siblings.filter(t => t.id !== taskId);
    
    // Calculate new taskOrder based on position
    let newTaskOrder: number;
    
    if (newPosition === 0) {
      // Moving to first position
      newTaskOrder = siblingsWithoutMoving.length > 0 
        ? (siblingsWithoutMoving[0].taskOrder || 1000) - 500 
        : 1000;
    } else if (newPosition >= siblingsWithoutMoving.length) {
      // Moving to last position
      newTaskOrder = siblingsWithoutMoving.length > 0 
        ? (siblingsWithoutMoving[siblingsWithoutMoving.length - 1].taskOrder || 1000) + 500 
        : 1000;
    } else {
      // Moving to middle position
      const beforeTask = siblingsWithoutMoving[newPosition - 1];
      const afterTask = siblingsWithoutMoving[newPosition];
      newTaskOrder = ((beforeTask.taskOrder || 1000) + (afterTask.taskOrder || 1000)) / 2;
    }
    
    // Update the task
    const updatedTask = {
      ...movingTask,
      taskOrder: newTaskOrder,
      parentTaskId: parentTaskId,
      lastModified: new Date().toISOString().split('T')[0],
    };
    
    mockTasks[taskIndex] = updatedTask;
    
    return updatedTask;
  }

