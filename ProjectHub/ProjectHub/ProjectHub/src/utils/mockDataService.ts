  static async updateTask(id: number, taskData: TaskFormData): Promise<Task> {
    await delay(500);
    
    const taskIndex = mockTasks.findIndex(t => t.id === id);
    if (taskIndex === -1) {
      throw new Error('Task not found');
    }

    const existingTask = mockTasks[taskIndex];
    const wasCompleted = existingTask.status === 'Completed';
    const isNowCompleted = taskData.status === 'Completed';

    // Filter out undefined properties from taskData in one concise operation
    const definedTaskData = Object.fromEntries(
      Object.entries(taskData).filter(([, value]) => value !== undefined)
    ) as Partial<TaskFormData>;

    // Handle special case for progress/status relationship
    let finalProgress = taskData.progress !== undefined ? taskData.progress : existingTask.progress;
    let finalStatus = taskData.status !== undefined ? taskData.status : existingTask.status;
    
    // Ensure status and progress are in sync
    if (finalProgress === 100 && finalStatus !== 'Completed') {
      finalStatus = 'Completed';
    } else if (finalProgress < 100 && taskData.progress !== undefined && finalStatus === 'Completed') {
      finalStatus = 'In Progress';
    } else if (finalStatus === 'Completed' && finalProgress < 100) {
      finalProgress = 100;
    }
    
    const updatedTask: Task = {
      ...existingTask,
      ...definedTaskData,
      // Always update these specific fields regardless
      progress: finalProgress,
      status: finalStatus,
      lastModified: new Date().toISOString(),
    };

    // Add project name for display
    const project = mockProjects.find(p => p.id === taskData.projectId);
    if (project) {
      updatedTask.projectName = project.name;
    }

    mockTasks[taskIndex] = updatedTask;

    // Update project completed task count if status changed
    if (wasCompleted !== isNowCompleted) {
      const projectToUpdate = mockProjects.find(p => p.id === taskData.projectId);
      if (projectToUpdate) {
        if (isNowCompleted) {
          projectToUpdate.completedTaskCount = (projectToUpdate.completedTaskCount || 0) + 1;
        } else {
          projectToUpdate.completedTaskCount = Math.max((projectToUpdate.completedTaskCount || 0) - 1, 0);
        }
      }
    }

    return updatedTask;
  }

